<?php 
	include 'includes/session.php';

	if(isset($_POST['id'])){
		$id = $_POST['id'];
		
		$conn = $pdo->open();

		$stmt = $conn->prepare("SELECT *, guidelines.id AS guideid, guidelines.name AS guidename, guidelines_category.name AS catname FROM guidelines LEFT JOIN guidelines_category ON guidelines_category.id=guidelines.category_id WHERE guidelines.id=:id");
		$stmt->execute(['id'=>$id]);
		$row = $stmt->fetch();
		
		$pdo->close();

		echo json_encode($row);
	}
?>